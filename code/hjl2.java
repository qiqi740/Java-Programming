public class hjl2 { 
        public static void main(String[] args) {
            // Initialize the sum variable
            int sum = 0;
    
            // Calculate the sum of numbers from 1 to 9
            for (int i = 1; i <= 9; i++) {
                sum += i;
            }
    
            // Display the result
            System.out.println("The result of 1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9 is: " + sum);
        
    }
    
}
